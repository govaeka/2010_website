<a name="naamkaartjes"></a><h2>Zorgen voor briefpapier, facturen en naamkaartjes</h2>

<p>

Ik weet het, briefpapier lijkt – behalve voor de occasionele fax – niet meer van deze tijd, maar wacht maar tot je aanmaningen en aangetekende schrijvens moet versturen. Kijk voor meer info bij <a href="freelance_moeilijkheden.php">Moeilijkheden</a>.<br />
Op je briefpapier, facturen en naamkaartjes moeten de volgende wettelijk verplichte gegevens voorkomen: naam, adres, bankrekeningnummer, btw-nummer, ondernemingsnummer (Vroeger was dat het handelsregisternummer. Klik <a href="http://www.bfak.be/KBO%20ondernemingsloket.htm"target="_blank">hier</a> voor meer info). Je kan ook zelf je modelbrieven, facturen en naamkaartjes opmaken en drukken, maar voor visitekaartjes zou ik toch de kleine investering doen. Voor zo’n 50 euro vind je online gemakkelijk 500 kaartjes van goede kwaliteit en het komt veel professioneler over dan je eigen knutselwerk. Op <a href="http://thoughtsontranslation.com/2010/03/05/some-thoughts-on-business-cards/"target="_blank">deze blog</a> vind je een hele post gewijd aan visitekaartjes. <br />
Het is natuurlijk niet de bedoeling dat je die kaartjes voortdurend onder de neus van je gesprekspartners duwt, maar heel dikwijls als je andere mensen ontmoet, komt het onderwerp “job” spontaan ter sprake. Op dat moment kan je hen een visitekaartje geven. Als zij op hun beurt iemand tegenkomen die een vertaler zoekt, geven ze je kaartje misschien wel door. Kijk voor meer “marketingtips” ook eens bij <a href="freelance_klantenwerving.php">klantenwerving</a>. 


</p>

<a name="boekhouder"></a><h2>Ga naar een goede boekhouder en/of belastingconsulent</h2>

<p>

Voor alles wat je koopt en wat ook maar van ver iets met je beroep te maken heeft, vraag je een factuur. Voorbeelden: visitekaartjes dus, woordenboeken, lexica, naslagwerken, autokosten, printerpapier, computer- en kantoorinfrastructuur, tv-blad voor tv-ondertitelaar, juridisch tijdschrift voor juridisch vertaler, medisch tijdschrift voor medisch vertaler. Zelfs al zullen je autokosten (brandstof bijvoorbeeld) maar gedeeltelijk als beroepskosten worden aangerekend, toch heb je facturen nodig. De Mister-Cash- of Bancontact-tickets van de automaten hebben voor de btw-diensten geen enkele waarde! Ofwel tank je altijd op dezelfde plaats en laat je de verkoper alles noteren tot hij een bepaald bedrag heeft om de factuur op te stellen, ofwel neem je een automatisch factureersysteem zoals dat van Q8, Texaco of Banksys (Mister Cash/Bancontact), waarmee je overal benzine, diesel of LPG kunt tanken en je maandelijks of halfmaandelijks een globale factuur van al je tankbeurten toegestuurd krijgt. Vergeet ook niet de huurkosten (of afschrijvingskosten) van je werkplaats en de kosten voor verwarming en elektriciteit. <br />

In principe zijn alle kosten aftrekbaar die helemaal of gedeeltelijk met je beroep te maken hebben. Voor de berekening van de verdeelsleutel bij gedeeltelijk aftrekbare kosten pleeg je het beste overleg met je boekhouder of belastingconsulent. Hetzelfde geldt voor de aftrekbare btw. Bovendien is het zo dat je maximaal 50% van de btw op je autobrandstof en autokosten mag aftrekken.<br />

Bij kleine aankoopbedragen rekenen sommige boekhandels administratiekosten aan. Desnoods betaal je die, om toch een factuur te hebben. Laat op de facturen voor beroepsliteratuur altijd de titels van de boeken en cd-roms specificeren, zodat de fiscus achteraf ziet dat je geen “nachtlectuur” hebt gekocht, maar wel degelijk boeken voor je beroep. <br />

Het kan dat bij je eerste belastingformulier noch een deel 2, noch een formulier voor de investeringsaftrek zit. Vraag die dan telefonisch bij de belastingdiensten aan. Als je nog geen zelfstandig hoofdberoep hebt uitgeoefend, hoef je in de eerste drie jaar van je zelfstandige beroepsactiviteit geen belasting vooraf te betalen om een belastingvermeerdering te vermijden.

</p>

<a name="bescherming"></a><h2>Bescherm je gegevens en je eigendom</h2>

<p>


Als je je auto ook voor je beroep gebruikt, moet je zeker bij je verzekeringsmakelaar je polis(sen) laten aanpassen. I.p.v. auto voor privégebruik: auto voor toerisme en zaken.<br /><br />

Als je een flat of huis huurt, moet in je huurcontract duidelijk vermeld staan dat je de flat of het huis niet alleen als woning, maar ook als kantoor (voor beroepsdoeleinden) gebruikt. Daardoor stijgt het kadastraal inkomen van de verhuurder en moet hij/zij meer belastingen betalen. Het is goed mogelijk dat hij/zij daarom een hogere huurprijs van je vraagt. Het verdient aanbeveling je huurcontract te laten registreren bij de Registratie en Domeinen.<br />
De brandverzekering van je woning dekt meestal niet de brandrisico’s, de waterschade en andere risico’s van je kantoormateriaal en bibliotheek. Daarvoor is een bijkomende polis nodig met opgave van alle toestellen (bouwjaar, merk, model, serienummer). In de verzekeringspolis staat gewoonlijk ook dat je een kopie van al je computerbestanden op een andere plaats moet bewaren. Het is aanbevolen elke week een back-up te maken op dvd, cd of externe harde schijf en die op een andere plaats (bv. in je bankkluis) te bewaren. Als je kantoor uitbrandt, ben je niet onmiddellijk al je computerbestanden kwijt. Ook back-ups via het internet zijn een mogelijkheid.


</p>

<a name="prijzen"></a><h2>Prijzen</h2>

<p>

Werk niet voor hongerlonen of dumpingprijzen, ook niet als beginnend vertaler. Je keldert er alleen je beroep en de kwaliteit van het vertaalwerk in het algemeen mee. Als je voor 0,50 of 0,60 EUR per regel werkt, win je hoogstens je basiskosten (computerinfrastructuur, printerpapier, printertoner, fax, tijd, enz.) terug en hou je onvoldoende over om in woordenboeken, cd-roms en infrastructuur (informatica, etc.) te kunnen investeren. Als je met zulke lage tarieven genoeg inkomsten wilt hebben, moet je bovendien zo oppervlakkig en vlug werken, dat je onmogelijk prima kwaliteit kunt afleveren. 
Als vele beginnende vertalers deze manier van werken hanteren, ondergraven ze allemaal samen de markt en vormen ze voor hun collega’s die al langer in het vak zitten, meer beroepservaring hebben en wel fatsoenlijke prijzen aanrekenen, geduchte (lees: oneerlijke) concurrenten. Het spreekt ook voor zich dat de algemene kwaliteit van het vertaalwerk eronder gaat lijden. <br />

Sommige vertalers hanteren een tarief per regel, per pagina of per woord. Er is ook een verschil tussen het berekenen van het tarief in de bron- of doeltaal. Over het algemeen wordt uitgegaan van het aantal woorden in de brontaal, maar soms is het handig toch voor de doeltaal te kiezen als de brontekst bijvoorbeeld niet digitaal beschikbaar is (iemand stuurt je bijvoorbeeld per post een kopie van een diploma) of als de woorden gewoon niet telbaar zijn (bijvoorbeeld een scan of een beveiligd pdf-bestand).  Hoe dan ook is het belangrijk dit goed en duidelijk af te spreken vooraleer je met de klant in zee gaat, om latere problemen te vermijden.<br />

Er zijn verschillende verdeelsleutels voor de berekening van het aantal regels. Voorbeeld 1: deel het aantal woorden door 9 voor Nederlands, Frans, Engels, Italiaans, … en door 8 voor Duits om het aantal regels te berekenen dat nodig is voor het factureren. Voorbeeld 2: tel het aantal tekens (inclusief spaties) en deel dat door 60 (als je uitgaat van een vertaalde regel van 60 aanslagen).
Voor je vertaling de deur uitgaat, moet je ze natuurlijk ook eerst nog “droog” nalezen. De ervaring van collega-vertalers leert dat je toch nog altijd een afgedrukte versie van je vertaling op fouten moet nalezen.<br />

Sommige klanten durven schaamteloos het onmogelijke te vragen. Een vertaling moet “tegen gisteren” geleverd worden, maar men wil niet extra betalen voor dringend werk of nacht- en weekendwerk en bovendien eist men nog een prima kwaliteit ook. Beding altijd haalbare en realistische deadlines, ook al verlies je daardoor misschien een mogelijke klant. Wie overweegt als zelfstandig vertaler aan de slag te gaan, moet ernstig met de ongunstige beroepsvooruitzichten (lees: keihard en onstandvastig werk voor een karige vergoeding en zeer lang wachten op betaling) rekening houden.<br />

Zorg ook voor degelijke leverings- en betalingsvoorwaarden, liefst in het Nederlands, Frans, Duits, Engels en/of de andere talen waaruit of waarin je vertaalt. Een Vlaamse vertaler die zijn klanten alleen Nederlandstalige verkoopsvoorwaarden voorschotelt, komt niet erg “talig” over.



</p>

<a name="specialisatie"></a><h2>Vertaal altijd naar je moedertaal en zoek een specialisatie</h2>

<p>


Dit is een grondregel bij het vertalen. In tegenstelling tot wat vaak nog algemeen wordt beweerd, kun je alleen maar goed vertalen in je eigen moedertaal. Vertalingen uit het Nederlands in het Frans, Engels, Duits, Italiaans, Spaans, Russisch, Japans, enz. speel je dus (al dan niet in onderaanneming) door aan je collega’s die deze respectievelijke talen als moedertaal hebben. <br /><br />
Van bij het begin moet je je als vertaler proberen te specialiseren. Een Nederlandstalige vertaler kan immers onmogelijk thuis zijn in zowel medische als juridische, politiek-economisch-sociale of andere vakgebieden. Als de vraag voor vertaalwerk in een vreemde taal (bv. Duits, Zweeds, een Slavische taal, …) groot is, als je denkt dat je het aankunt en als je klant tevreden is over je vertalingen, dan zou het natuurlijk dom zijn niet tegen de bovenvermelde grondregel te willen zondigen. De uitzondering bevestigt immers de regel. Vooral voor technisch vertaal- en tolkwerk is intensief contact met je klant heel belangrijk. Aarzel niet om bij je klant te informeren, als bepaalde elementen in de brontekst niet helemaal duidelijk zijn.


</p>


<a name="technologie"></a><h2>Technologische hulpmiddelen</h2>

<p>

Heel handig zijn woordenboeken op cd-rom, zoals het grote Van Dalewoordenboek Nederlands, het Nederlands synoniemenwoordenboek en de grote Van Dalevertaalwoordenboeken Engels, Duits en Frans. Je kunt de hele inhoud ervan zelfs op je harde schijf zetten. Een absolute must is natuurlijk het Elektronische Groene Boekje, een elektronische versie van de groene bijbel met de nieuwe Nederlandse spelling. Ook een gegevensbank voor glossariumbeheer en –raadpleging is uiterst nuttig. Je kunt er voor elke klant afzonderlijke “steekkaartenbakken” of glossaria mee aanmaken en in je vrije momenten kun je die terminologiebestanden aanvullen met relevante terminologie. Er bestaan computerprogramma’s (CAT-tools als Déjà Vu, Trados, enz.) waarmee je bronteksten semiautomatisch kunt vertalen. Meer informatie daarover vind je in <a href="http://www3.imperial.ac.uk/portal/pls/portallive/docs/1/7307707.PDF"target="_blank">dit rapport</a> uit 2006. Dergelijke vertaalgeheugens zijn intussen betaalbaar geworden, maar toch zijn ze alleen geschikt voor vertalers die voortdurend dezelfde teksten (handleidingen van machines, notariële akten, notulen van vergaderingen, circulaires, bijsluiters, enz.) te vertalen krijgen. Verwacht van die software ook geen wonderen. Vooral in de beginfase moet je als vertaler heel vaak zelf kiezen uit de door het vertaalgeheugen aangeboden mogelijke vertalingen en ook veel vertalingen toevoegen aan de vertaalgeheugens. Hoe meer je een dergelijk programma gebruikt, hoe meer vertalingen de computer onthoudt. Hij zal je almaar minder om oplossingen vragen.<br />

Zoals reeds gezegd, koop je best een toestel dat tegelijk kan printen, faxen en fotokopiëren. Je kunt ook niet zonder antwoordapparaat. Hou je boodschap daarop kort en bondig.  Wees zeer selectief met het meedelen van je gsm-nummer. Sommige klanten deinzen er niet voor terug je ’s avonds laat of zelfs in het holst van de nacht op je gsm te bellen. Je kunt natuurlijk ook altijd nog je gsm uitzetten. 



</p>



<a name="beediging"></a><h2>Werken als beëdigd vertaler</h2>

<p>

Als vertaler kan je je laten beëdigen zodat je beëdigde vertalingen kan maken. Daarna wordt aan je vertalingen een grotere waarde gehecht.<br />

Enkel een beëdigd vertaler mag vertalingen maken die beëdigd moeten zijn, zoals documenten die ingediend moeten worden bij een Immigratie- of Naturalisatiedienst (zoals geboorte-, huwelijks- en scheidingsaktes), officiële transcripties (zoals middelbareschooldiploma’s, academische graden en vakdiploma’s), regulariserende documenten (zoals bevoegdheidsverklaringen, protocollen, onderzoeksgegevens en processtukken), patenten en nog veel meer.<br />

Een beëdigde vertaling in private context wordt vergoed tegen een tarief dat hoger ligt dan de prijs die meestal wordt toegepast voor niet-beëdigde vertalingen, omdat het onontbeerlijk is een goede kennis te hebben  van de rechtspraak en zich te laten bijstaan door juristen om een kwaliteitswerk te kunnen afleveren. Soms heeft deze vertaling niets te maken met het gerechtelijke of juridische vakgebied. Bepaalde klanten vragen “een beëdigde vertaling” om er zeker van te zijn dat de tekst wel degelijk helemaal en trouw werd vertaald en om zich in te dekken tegen fouten van de vertaler. Zo kunnen de vertalingen van bijvoorbeeld een cv, de bijsluiter van een geneesmiddel, commerciële brieven, enz. beëdigd worden.<br />

Om beëdigd te worden als vertaler, moet je contact opnemen met de Voorzitter van de rechtbank van eerste aanleg of de Eerste Raadgever van het Gerechtshof van je woonplaats (Justitiepaleis van je woonplaats, Rechtbank van eerste aanleg) met een fotokopie van je diploma(‘s) en/of andere documenten die het bewijs leveren dat je de talen waarvoor je beëdigd wil worden, beheerst. Op vraag van het Parket zal de kandidaat worden opgeroepen door de politie voor een administratieve ondervraging. De Algemene Vergadering van de Rechtbank of van het Gerechtshof beslist over de ontvankelijkheid van de kandidatuur, benoemt je dan tot “Beëdigd vertaler bij de Rechtbank van eerste aanleg of het Gerechtshof van … “ en nodigt je uit om de eed te komen afleggen tijdens een openbare zitting. Pas na de eedaflegging mag je als beëdigd vertaler beëdigde vertalingen uitvoeren in de talen waarvoor je bent beëdigd.



</p>
				
