<a name="statuut"></a><h2>Werknemer of freelancer?</h2>

<p>

Wie wil werken als vertaler, kan gaan voor een functie als werknemer bij een vertaalbureau of een ander bedrijf. Dat noemen we dan een in-housevertaler. Maar je hebt ook de keuze om als zelfstandig vertaler voor je eigen rekening te gaan werken, dus als freelancer. <br />

Als je werkt als freelancer heb je dan weer twee soorten van klanten: vertaalbureaus en je eigen klanten. Vertaalbureaus hebben het voordeel dat je een redelijk constante workflow krijgt en dat er rekening gehouden wordt met jouw specialisaties. In de praktijk zal je als freelance vertaler waarschijnlijk ook het meest via vertaalbureaus werken. Kijk voor meer info ook eens bij <a href="freelance_klantenwerving.php">klantenwerving</a>.<br /><br />

Daarom lijkt het me interessant eerst het freelancestatuut aan te vragen voordat je begint te solliciteren naar een in-housefunctie.  Op die manier kan je solliciteren bij vertaalbureaus als in-house medewerker en vermelden dat, als dat niet mogelijk is, je ook freelance voor hen kan werken.<br />

Uiteraard kan je ook beide combineren en freelancer worden in bijberoep, al weet ik niet of je werkgever ermee gediend zal zijn dat zijn werknemer buiten de werkuren zijn concurrent wordt.<br />


</p>


<a name="procon"></a><h2>Voor- en nadelen van het werken als zelfstandige</h2>

<p>
Opmerking: Dit is een kort overzichtje van positieve en negatieve kanten van het werken als freelancer. Neem gerust de rest van de site eens door en maak dan uit of het iets voor jou is. Dit lijstje kan je wel al een ruw idee geven.<br /><br />

Werken als freelancer is voor vele mensen een aantrekkelijke gedachte. Het roept dan ook dikwijls een beeld op van doen wat je wilt en wanneer je wilt. 
</p>
<ul>
<li>Beginnen je kantooruren om negen? Lekker om kwart voor negen uit je bed en je in je pyjama voor je pc nestelen. </li>
<li>Geen baas die over je schouder meekijkt en vraagt wanneer dat werk af zal zijn.</li>
<li>Geen collega’s die ’s ochtends net iets te luidruchtig zijn, of anderen die een knoert van een ochtendhumeur hebben, of gewoon collega’s die je voortdurend van je werk houden.</li>
<li>Verplaatsingstijd om naar het werk te gaan: nul minuten. Geen files, geen treinvertragingen.</li>
<li>Zin om op vakantie te gaan? Je hoeft je klanten niet per se te zeggen dat je weg bent. Neem gewoon je laptop en gsm mee. Op die manier kan je het hele jaar door op reis als je dat wilt.</li>
<li>Je startkapitaal is heel beperkt. Je hebt geen kantoorruimte nodig, je woonkamer is prima. Meestal heb je genoeg aan de dingen die je sowieso in huis hebt: een laptop, gsm, internetverbinding, vaste telefoonlijn, printer. Natuurlijk heb je wel nog woordenboeken nodig en wat extra software, maar dat is een eenmalige kost en valt al bij al nog goed mee.</li>
<li>Je kan een beetje aan “creatief boekhouden” doen. Je facturen voor gsm of internet kan je bijvoorbeeld zonder problemen als kost inbrengen. </li>
<li>Je kan het huishouden tussendoor doen. Als pauze snel even de vaatwasser leeghalen? Waarom niet!</li>
</ul>
<p>
Spijtig genoeg ziet niet alles er zo rooskleurig uit. Aan het werken als freelancer zijn ook heel wat nadelen verbonden. Ieder moet voor zichzelf uitmaken of hij daarmee kan leven.
</p>
<ul>
<li>Hét grote nadeel aan het werken als zelfstandige, is het financiële nadeel. Om te beginnen word je betaald voor je werk, niet voor de uren die je presteert. Zit je aan een vertaling dubbel zo lang als je gedacht had? Pech gehad, je zal er niet meer geld voor krijgen. Dagje ziek? Of gewoon een baaldag? Niemand zal het voor je betalen. </li>
<li>Vooral freelancers die werken met eigen klanten moeten opletten dat ze niet te veel vertrouwen op één belangrijke klant die hen veel werk geeft. Als die klant wegvalt, zie je je grootste bron van inkomsten in rook opgaan en zit je als vertaler in de problemen.</li>
<li>Je moet constant tijd en geld investeren in promotie. Het is echt niet gemakkelijk potentiële klanten te overtuigen met jou samen te werken, zeker als je nog geen werkervaring hebt. Geen klanten, geen werk, geen inkomen.</li>
<li>Een gevolg van al deze financiële onzekerheid: probeer maar eens een lening te krijgen om een huis te kopen. Veel succes!</li>
<li>Naast de “gewone” risico’s, speelt je persoonlijkheid ook een grote rol. Je moet vlot overkomen aan de telefoon om je klant een vertrouwde en vriendelijke indruk te geven.</li>
<li>Tegelijkertijd moet je iemand zijn die er geen problemen mee heeft om de hele dag geen mens te zien. </li>
<li>Je moet initiatief nemen. Zowel wat betreft promotie maken en klanten lokken, als wat het werk zelf betreft. Ga je uitstellen en veel pauzes nemen zodat je er een eeuwigheid over doet om iets af te krijgen, is het misschien beter dat je een job in loonverband zoekt, waarbij je de druk hebt van een baas of collega’s die wachten op je werk.</li>
<li>Ben je iemand die nood heeft aan erkenning, een schouderklopje op regelmatige basis en liefst zo veel mogelijk pluimpjes? Die zal je als freelancer ook zelden krijgen.</li>
<li>Je moet al je paperassen zorgvuldig bijhouden. Meestal volstaat het wel om één keer per jaar een boekhouder je balans te laten opmaken, maar dan moet al het materiaal natuurlijk wel goed geordend zijn.</li>
</ul>



				
