<h1>De papiermolen</h1>

<p>

Ok, je hebt beslist dat je als freelancer aan de slag wil. Hoe begin je daar nu aan?<br /><br />

Hieronder staan een aantal formaliteiten die je in orde dient te brengen als je als freelancer aan de slag wil. Als je een vennootschap wil opstaren en eventueel personeel aanwerven, komen hier nog een paar formaliteiten bij. Kijk daarvoor op de website van de UNIZO startersservice: <a href="http://www.startersservice.be"target="_blank">www.startersservice.be</a><br />
<br />

Om het freelancestatuut te bekomen, moet je het volgende doen:<br /><br />

1 <a href="#bankrekening">een bankrekening openen </a><br />
2 <a href="#kbo">je inschrijven bij de Kruispuntbank voor Ondernemingen (KBO).</a><br />
3 <a href="#ondernemingsnummer">het ondernemingsnummer activeren bij de BTW</a><br />
4 <a href="#verzekeringen1">je aansluiten bij een sociaal verzekeringsfonds</a><br />
5 <a href="#verzekeringen2">je aansluiten bij een ziekenfonds</a><br />





<a name="bankrekening"></a><h2>Een bankrekening openen</h2>

<p>
Open bij een financiële instelling een zakenrekening. Regel uitgaven die helemaal of gedeeltelijk met je beroep te maken hebben, altijd via die bankrekening (Mister Cash/Bancontact, kredietkaarten, cheques, giro’s). Dat maakt de controle van je beroepsuitgaven voor jezelf, de fiscus en de btw-controle veel gemakkelijker. Het nummer van deze rekening en de naam van de financiële instelling moet je vermelden op al je facturen, brieven, bestelbons, enzovoort. Doe je dat niet, dan kan je slechte betalers geen nalatigheidsintresten aanrekenen. </p>

<a name="kbo"></a><h2>Je inschrijven bij de Kruispuntbank voor Ondernemingen (KBO)</h2>

<p>
Vroeger moest je naar de griffie van de rechtbank van koophandel voor inschrijving in het handelsregister. Als startende zelfstandige moet je je onderneming inschrijven in de Kruispuntbank van de Ondernemingen. Hiervoor kan je terecht bij het ondernemingsloket op <a href="http://www.zenito.be"target="blank">www.zenito.be</a>. Je krijgt dan een uniek ondernemingsnummer. Dit nummer zal na verloop van tijd alle andere identificatienummers vervangen. De Kruispuntbank vervangt het vroegere handels- en ambachtsregister. De overheid gebruikt voortaan het unieke ondernemingsnummer in alle communicatie naar jou toe.<br />
Je firmanaam kies je helemaal zelf. As je dat niet doet, beslist de ambtenaar zelf en krijg je meestal “familienaam + Vertalingen” als firmanaam toegewezen. Je kunt natuurlijk ook zelf een firmanaam kiezen, maar dan moet je wel eerst nagaan of die niet reeds geregistreerd is. Namen als “logos”, “international”, “translations”, “traductions”, “lingua” worden uiteraard veelgebruikt.
</p>

<a name="ondernemingsnummer"></a><h2>Het ondernemingsnummer activeren bij de BTW</h2>

<p>
Zodra je je ingeschreven hebt in de Kruispuntbank en je ondernemingsnummer gekregen hebt, moet je ook je BTW-status activeren. Ook dit gebeurt via een erkend ondernemingsloket zoals het Zenito Ondernemingsloket. Het ondernemingsnummer doet dan dienst als BTW nummer. Als vertaler kies je het beste voor een driemaandelijkse btw-aangifte. Iedere maand moet je voor de 20ste je btw vooruitbetalen. Omdat je als starter moeilijk kunt weten hoeveel btw je aan het einde van het eerste kwartaal zult moeten betalen aan of krijgen van vadertje Staat, kun je het best minimaal 250 EUR per maand vooruitbetalen aan de btw. Voor 20 april, 20 juli, 20 oktober van elk jaar en voor 20 januari van het daaropvolgende jaar moet je een kwartaalaangifte invullen en opsturen. Het komt er dus op aan regelmatig je boeken van aan- en verkoopfacturen in te vullen om je btw-aangifte te kunnen indienen. Voor vertaal- en tolkwerk gelden de volgende btw-tarieven:<br />
<ul>
<li>21% voor copywriting, vertaalwerk, tekstaanpassing, enz. en ook voor tolkwerk;</li>
<li>25% voor verkoop van ingebonden en gefotokopieerde statuten, ingesproken audiocassettes, enz.;</li>
<li>6% voor vertalingen die een auteursrechtelijk karakter hebben en die voor publicatie of uitzending bestemd zijn, zoals: tv-ondertiteling, enz. (op factuur vermelden: “6% btw overeenkomstig art. 18, § 1, 7° van het btw-wetboek en rubriek XXIX van tabel A van het KB nr. 20 van 20/07/1970”)</li>
<li>0% voor tolkwerk, maar dan alleen nog in de volgende specifieke gevallen:</li>
<ul><li>ter uitvoering van een gerechtelijke opdracht (op factuur vermelden: “Vrijstelling van btw overeenkomstig de ministeriële brief nr. 15 van 27 juli 1979, punt 22”)</li>
<li>in het raam van een openbaar en vrij voor het publiek toegankelijke conferentie, spreekbeurt of uiteenzetting, voor zover de voordrachtgever en de tolk allebei handelen voor rekening van de organisator van die conferentie (op contract en factuur vermelden: “Vrijstelling van btw overeenkomstig art. 44, §2, 8° van het btw-wetboek” + “Voor rekening van de organisator van de conferentie, die vrij toegankelijk is voor het publiek”);</li>
<li>als je vertaalt of tolkt voor een btw-plichtige uit een andere EU-lidstaat. Je moet wel uitdrukkelijk het btw-nummer van je Duitse, Nederlandse, Franse, Britse, etc. klant op je factuur vermelden en ook: “Vrijstelling van Belgische btw overeenkomstig art. 21, §3, 7° van het Belgische btw-wetboek. De Duitse/Nederlandse/Frans/Britse/etc. btw moet u in Duitsland/Nederland/Frankrijk/Groot-Brittannië/etc. voldoen.”</ul></li></li></ul>
</p>

<a name="verzekeringen1"></a><h2>Je aansluiten bij een sociaal verzekeringsfonds</h2>

<p>
Als zelfstandige, in hoofd- én bijberoep, moet je je ook aansluiten bij een sociaal verzekeringsfonds. Dit fonds int de sociale bijdragen die je moet betalen en zorgt ervoor dat de uitkeringen correct gebeuren. De aansluiting moet gebeuren binnen de 90 dagen na het starten van je zaak. Je kan dat online doen bij Zenito Sociaal Verzekeringsfonds, het Sociaal Verzekeringsfonds voor Zelfstandingen. <br />
Elk kwartaal betaal je aan het sociale verzekeringsfonds een bijdrage die berekend wordt op je activiteiten van drie jaar terug. Tijdens de eerste drie jaar is dat natuurlijk niet mogelijk en wordt er meestal een basisbedrag gevraagd. Dat is voor de eerste drie jaar respectievelijk 631,76, 647,16 en 662,57 euro voor zelfstandigen in hoofdberoep en 69,89, 71,60 en 73,30 euro voor zelfstandigen in bijberoep. (cijfers voor 2009. Bron: <a href="brochureAfgestudeerd-wat-nupdf.pdf"target="_blank">Stuvo Brochure: Afgestudeerd! Wat nu?.pdf</a>)<br />
Een tweede mogelijkheid is dat je sociale bijdragen betaalt op basis van een geschat inkomen. Als je bij de start van je zelfstandige activiteit verwacht dat je inkomen hoger zal liggen dan het forfaitaire inkomen, kan je aan je sociaal verzekeringsfonds vragen om je voorlopige bijdragen te laten berekenen op je vermoedelijk inkomen en dus hogere bijdragen betalen. Voordelen hiervan zijn de regularisatie na drie jaar minder zwaar maken, een fiscale winst (want de sociale bijdragen zijn fiscaal aftrekbaar) en een betere planning van de lasten. Indien uit de regularisaties blijkt dat je te veel bijdragen betaalde, krijg je het teveel betaalde saldo terugbetaald. Bovendien krijg je ook een starterbonus toegekend voor de bijdragen die je betaald hebt bovenop de minimumbijdrage.<br />
</p>

<a name="verzekeringen2"></a><h2>Je aansluiten bij een ziekenfonds</h2>

<p>
Om in orde te zijn met je ziekteverzekering ben je verplicht je aan te sluiten bij een ziekenfonds. Als je al aangesloten bent, dien je een wijziging van je statuut te melden. In vergelijking met het sociaal zekerheidsstelsel voor werknemers en ambtenaren is het sociaal statuut der zelfstandigen slechts een minimumstelsel. Daarom wordt aangeraden je aanvullend te laten verzekeren. Zo kun je voor de veiligheid volgende bijkomende verzekeringen afsluiten: kleine risico’s en arbeidsongeschiktheid (na 3 maanden volledige arbeidsongeschiktheid krijg je naast je riziv-uitkering nog een bijkomende uitkering van het ziekenfonds). <br />
Ook voor pensioenen is er een aparte regeling voor zelfstandigen, maar het is wel mogelijk deze persoonlijk aan te vullen. Je bijdragen voor een dergelijk pensioen zijn ook aftrekbaar tot een maximaal bedrag dat afhangt van je belastbaar inkomen. Ook een hospitalisatieverzekering is aan te raden, maar die kun je niet aftrekken. Als je al een paar jaartjes in het vak zit, kun je misschien ook een verzekering voor een gewaarborgd minimuminkomen afsluiten. Het aftrekbare bedrag ervan hangt van je inkomen af.<br />



</p>



				
