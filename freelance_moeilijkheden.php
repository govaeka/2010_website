<h1>Moeilijkheden</h1>

<p>

Spijtig genoeg kan je het als vertaler soms erg lastig krijgen. Dit zijn de grootste obstakels die je kan tegenkomen:<br />


1 <a href="#proefvertalingen">proefvertalingen</a><br />
2 <a href="#betalingen">laattijdige betalingen</a><br />


<a name="proefvertalingen"></a><h2>Proefvertalingen</h2>

<p>

Sommige klanten vragen bij het bestellen van een opdracht een proefvertaling. Daarmee wil de klant een idee krijgen van je prestaties en deze beoordelen. Als je beslist daar op in te gaan is het aangeraden deze proefvertaling te beperken tot maximum één pagina om misbruiken te vermijden. Je kan bijvoorbeeld een willekeurig gedeelte van de tekst vertalen of een vertaling opsturen die representatief is voor het werk. <br />
Spijtig genoeg wordt hierbij dikwijls misbruik gemaakt van de vertaler. Een klant zal vragen een stuk van de tekst als proef te vertalen, waarna hij laat weten dat je niet aangenomen bent omdat de kwaliteit niet goed was, maar de vertaling zonder je medeweten toch gebruikt. Daarom is het aan te raden bij een nieuwe, onbekende klant na te gaan of het aanbod ernstig is en de klant solvabel is. Dat kan je nagaan bij je collega’s of bij nieuwsgroepen op het internet. Voor grote opdrachten kan je ook de balansen opvragen bij de Nationale Bank van België (wel enkel voor Belgische vennootschappen). Ook bij andere bedrijven kan je handelsinlichtingen opvragen. Deze zijn meestal niet goedkoop, maar bij grote opdrachten kan dit je veel ellende besparen. Een voorbeeld is de firma Dun & Bradstreet. Waar je dan moet naar uitkijken, is of de klant al geprotesteerde wissels heeft gehad. Is dat het geval, dan kun je die mogelijke klant maar beter meteen wandelen sturen. Leden van de Belgische Kamer van Vertalers, Tolken en Filologen kunnen bij de <a href="http://www.cbtip-bkvtf.org/index.php?lang=du"target="_blank">BKVTF</a> informeren naar de reputatie van een eventuele nieuwe of bestaande klant.<br /><br />



</p>

<a name="betalingen"></a><h2>Laattijdige betalingen</h2>

<p>

Als zelfstandige vertaler moet je geduldig wachten tot de klanten betalen. Betaaltermijnen als einde maand +30 dagen of zelfs +60 dagen zijn jammer genoeg almaar meer de regel, ook al gaat het soms om zeer kleine bedragen. Reageer altijd (eerst telefonisch, dan per e-mail en daarna per aangetekende brief met ontvangstbevestiging en ook per gewone post) op onbetaalde facturen. Aarzel niet naar de rechter te stappen om nalatigheidintresten en schadevergoeding te eisen, ook al gaat het om kleine bedragen. Je werk hoort betaald te worden, zeker als je snel moet leveren.<br />

Zorg daarom altijd voor een schriftelijke opdrachtbevestiging, met inbegrip van de algemene voorwaarden. Hou de betaaltermijnen goed in het oog en reageer altijd bij onbetaalde facturen.<br /><br />

Ook op vertalersfora wordt hierover natuurlijk duchtig gediscussieerd en informatie uitgewisseld. Dit is een voorbeeldje van iemand die haar gouden tips deelt:

<blockquote>
“I have established a 4-point checklist to analyze a new agency/client's likelihood of turning out to be a bad payer:
<ol>
<li>BlueBoard check (at least five entries)</li>
<li>I do not trust an agency/client that hires me for a large job for the first time. Serious people would send first some small jobs so they can confirm your seriousness, reliability and quality. This works the same way for me: with small jobs I do not risk much and can also confirm the seriousness and honesty of the agency/client.</li>
<li>I do not trust an agency/client that stresses the necessity of a "good quality" job on the announcement or job proposition. Good quality is intrinsic to the business–nobody would hire anybody, translator, plumber or whatever, to do a bad job. This statement is a good sign that the agency/client wants to have an edge to be able to claim later a rebate or non-payment due to the quality of the job. Most of the time the quality of a text or translation is subjective, and the proofreader's assessment obviously cannot be taken as final, since generally he/she is your competitor.</li>
<li>I do not trust an agency/client that offers a higher than average rate. If something is too good to be true, it generally is.</li></ol>
Of course these four points are not foolproof but they have served me well over the years.”
</blockquote>

Dit berichtje werd gepost op de website <a href="http://www.proz.com"target="_blank">www.proz.com</a> waar je een van de populairste forums vindt voor vertalers. In de afdeling BlueBoard kan je een beoordeling kwijt over je opdrachtgevers (zowel positief als negatief).


</p>

				
