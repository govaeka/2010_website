<a name="glossaria"></a><h2>Glossaria en woordenboeken</h2>

	<p>
		<a name="meertalig"></a><h3>Meertalig</h3>
		<p>
		<a href="http://www.ockers.be/index.html">http://www.ockers.be/index.html</a><br />
		Luc Ockers is een vertaler-tolk die zijn glossaria en al wat hij in zijn carrière vergaard heeft, beschikbaar maakt op zijn website. Zo kan je een lijst vinden met begrippen uit Belgisch-Nederlands versus Belgisch-Duits, een glossarium voor dierennamen, diamantterminologie, … Zeker de moeite waard om deze site eens te bekijken!<br /><br />
		<a href="http://translate.google.be/?sl=auto&tl=nl#">http://translate.google.be</a><br />
De vertaler van Google. Volgens mij nog steeds de beste online vertaler. Voor teksten in een taal die je niet begrijpt, kan het handig zijn om een idee te krijgen van de inhoud.<br /><br />
		<a href="http://www.vertalen.nu/">http://www.vertalen.nu</a><br />
Online vertalend woordenboek met Nederlands als basistaal.<br /><br />
		<a href="http://vertalen.startpagina.be/">http://vertalen.startpagina.be</a><br />
Heel veel links naar allerhande sites die ook maar iets te maken hebben met vertalen of vertalers. Misschien zit er wel iets tussen dat je zoekt.<br /><br />
		<a href="http://eur-lex.europa.eu/">http://eur-lex.europa.eu</a><br />
Een van mijn persoonlijke favorieten. Op deze website van het Recht van de Europese Unie vind je alle teksten die binnen de EU gepubliceerd zijn en dat in verschillende talen, die je naast elkaar op je scherm kan weergeven. Het gaat om wetteksten, maar aangezien wetteksten nooit alleen over wetten gaan, maar over uiteenlopende onderwerpen, vind je er veel informatie in terug. Dikwijls ook gespecialiseerde termen.<br /><br />
		<a href="http://wordreference.com/ende/">http://wordreference.com</a><br />
Oxford woordenboek Engels – Duits<br /><br />
		<a href="http://www.thefreedictionary.com/">http://www.thefreedictionary.com</a><br />
Woordenboek in verschillende talen. Je kan ook zoeken op rubriek: medical, legal, idioms, …<br /><br />
		<a href="http://iate.europa.eu/">http://iate.europa.eu</a><br />
De veeltalige databank van de EU.<br /><br />
		<a href="http://www.proz.com/search/">http://www.proz.com/search</a><br />
Proz is een website voor vertalers. Er is ook een toepassing waarmee je raad kan vragen aan andere vertalers als je een bepaalde term echt nergens vindt. Op basis van al die termen waarover al gediscussieerd is, hebben ze een databank opgesteld waarin je via deze link kan zoeken. Alledaagse woorden zal je er dus niet in vinden, enkel de “vertaalproblemen”. Heel handig als iemand jouw “vertaalprobleem” al eerder gehad heeft.<br /><br />
		</p>
		<a name="eentalig"></a><h3>Eentalig</h3>
		<p>
		<a href="http://www.finquest.nl/recent-toegevoegd-wb.html">http://www.finquest.nl</a><br />
Economisch verklarend woordenboek. Zoek alfabetisch of op rubriek: beleggen, economie, fiscaal, juridisch, marketing en communicatie, …<br /><br />
		<a href="http://www.ing.com/group/showdoc.jsp?docid=182991_EN&htmlid=185191_EN&menopt=abo|glo">http://www.ing.com/group/showdoc.jsp?docid=182991_EN&htmlid=185191_EN&menopt=abo|glo</a><br />
Het glossarium van ING. Er is zowel een Engels als een Nederlands glossarium, maar spijtig genoeg zijn ze niet gelinkt. Het is dus geen vertaler.<br /><br />
		<a href="http://www.merriam-webster.com/">http://www.merriam-webster.com</a><br />
Het verklarend Engels woordenboek Merriam Webster geeft je ook de kans om de woorden te beluisteren.<br /><br />
		<a href="http://www.urbandictionary.com/">http://www.urbandictionary.com</a><br />
Dé referentie voor termen uit de populaire cultuur, dialect, slang. Iedereen kan woorden toevoegen, dus neem wat je vindt met een korreltje zout. Maar het kan je wel in de juiste richting sturen.<br /><br />
		<a href="http://synoniemen.net/index.php">http://synoniemen.net</a><br />
Voor wie een wat kleurrijker taalgebruik wil hanteren.<br /><br />
		<a href="http://www.roche.de/lexikon/index.htm?loc=www.roche.de&content=/lexikon/">http://www.roche.de/lexikon/index.htm?loc=www.roche.de&content=/lexikon/</a><br />
Verklaring van medische begrippen in het Duits.<br /><br />
		<a href="http://www.scta.be/">http://www.scta.be</a><br />
De centrale dienst voor Duitse vertaling in België. Ze vertalen Belgische wetboeken, wetten, besluiten en omzendbrieven. Daardoor kwam er een uitgebreide databank tot stand (semamdy) die op deze website ter beschikking gesteld wordt. Daarnaast is er een kleinere, juridische databank, debeterm, waarin de rechtsterminologie vastgelegd wordt die voor de Belgische overheden bindend is.  Je kan zoeken tussen de drie landstalen. <br /><br />
		<a href="http://www.canoo.net/">http://www.canoo.net</a><br />
Deutsche Wörterbücher und Grammatik. Canoo heeft een woordenboek van meer dan 250 000 woorden en meer dan drie miljoen woordvormen. Als je op woordenboeken klikt, zie je een lijst met nog vele andere online woordenboeken. Bovendien geeft Canoo een bijdrage over spelling en een schat aan informatie over  grammatica: Wortgrammatik, Wortbildung en Satzgrammatik.<br /><br />
		</p>
	</p>	
	
<a name="grammatica"></a><h2>Grammatica en andere info</h2>
		<p>
		<a name="engels"></a><h3>Engels</h3>
		<p>
		<a href="http://www.wsu.edu/~brians/errors/errors.html">http://www.wsu.edu/~brians/errors/errors.html</a><br />
Common Errors in English Usage. Zeer handige website! Een heel glossarium van woorden die dikwijls voor verwarring zorgen, zoals advice/advise of farther/further. <br /><br />
		<a href="http://esl.fis.edu/grammar/index.htm">http://esl.fis.edu/grammar/index.htm</a><br />
Een website over Engelse grammatica, met oefeningen, testjes, … Omdat Engels niet zo simpel is als de meesten denken. En omdat op een leuke manier je grammatica opfrissen nooit kwaad kan.<br /><br />		
		</p>
		<a name="nederlands"></a><h3>Nederlands</h3>
		<p>
		<a href="http://www.let.ru.nl/ans/e-ans/">http://www.let.ru.nl/ans/e-ans/</a><br />
De elektronische ANS: Algemeen Nederlandse Spraakkunst. Hét referentiewerk voor al je twijfels over grammatica.<br /><br />
		<a href="http://taalunieversum.org/">http://taalunieversum.org</a><br />
Vele links op de homepage naar alles wat ook maar iets met de Nederlandse taal te maken heeft. Vooral de links bij Taal zijn interessant, onder andere de subsites <a href="http://woordenlijst.org/">http://woordenlijst.org</a> en <a href="http://taaladvies.net/">http://taaladvies.net</a>.<br /><br />
		<a href="http://www.onzetaal.nl/ot/index.php">http://www.onzetaal.nl</a><br />
Een hele site voor liefhebbers van het Nederlands. De voornaamste activiteit van het Genootschap Onze Taal is het uitgeven van hun tijdschrift, maar ze hebben ook een Taaladviesdienst, en geven door middel van congressen, boeken en een website voorlichting over het Nederlands.<br /><br />
		<a href="http://taal.vrt.be/taaldatabanken_master/taalkwesties/v-vz/tk-v0012.shtml">http://taal.vrt.be/taaldatabanken_master/taalkwesties/v-vz/tk-v0012.shtml</a><br />
Heel deze website is interessant, maar deze pagina geeft je een mooi lijstje van vaste uitdrukkingen zoals heden ten dage, in goeden doen, in levenden lijve, te allen tijde, …<br /><br />		
		</p>
		<a name="duits"></a><h3>Duits</h3>
		<p>
		<a href="http://www.canoo.net/">http://www.canoo.net</a><br />
Deutsche Wörterbücher und Grammatik. Canoo heeft een woordenboek van meer dan 250 000 woorden en meer dan drie miljoen woordvormen. Als je op woordenboeken klikt, zie je een lijst met nog vele andere online woordenboeken. Bovendien geeft Canoo naast het woordenboek nog een bijdrage over spelling en een schat aan informatie over  grammatica: Wortgrammatik, Wortbildung en Satzgrammatik.<br /><br />
		<a href="http://wortschatz.uni-leipzig.de/">http://wortschatz.uni-leipzig.de</a><br />
De ideale site om de context te kennen waarin een woord gebruikt wordt. Je vindt er de meest voorkomende linker- en rechterburen (heel handig voor onder andere voorzetsels) alsook korte stukjes tekst uit het corpus, zodat de betekenis duidelijk wordt. <br /><br />


		
		
		
